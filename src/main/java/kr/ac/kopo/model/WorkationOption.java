package kr.ac.kopo.model;

public class WorkationOption {
	private int workationOptionId;
	private String workationName;
	private int price;
	private String detail;
	public int getWorkationOptionId() {
		return workationOptionId;
	}
	public void setWorkationOptionId(int workationOptionId) {
		this.workationOptionId = workationOptionId;
	}
	public String getWorkationName() {
		return workationName;
	}
	public void setWorkationName(String workationName) {
		this.workationName = workationName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getDetail() {
		return detail;
	}
	public void setDetail(String detail) {
		this.detail = detail;
	}
	
	
	
}
